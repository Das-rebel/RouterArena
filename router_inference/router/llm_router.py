"""RouterArena adapter for `claude-code-llm-router`.

This adapter ports the production heuristic classifier from
`llm-router/src/llm_router/hooks/auto-route.py` and maps the
(task_type, complexity) pair to one of four cloud models in the
RouterArena lean pool: gpt-4o-mini, gpt-4o, claude-3-haiku-20240307,
gemini-2.0-flash-001.

Design notes
------------
- **No network calls during routing.** The production router uses a
  multi-layer classifier (regex → Ollama → cheap-API). For RouterArena we
  use only the deterministic regex layer, so submissions are reproducible
  and add zero classifier cost to the eval run.
- **Ollama collapse.** In production, simple/moderate tasks first try a
  free local Ollama model. RouterArena prices everything via API, so we
  collapse Ollama predictions to `gpt-4o-mini` (the cheapest cloud model
  in the lean pool) per the user's pre-submission decision.
- **Safe fallback.** Any classification failure returns `gpt-4o-mini` —
  the same fallback the production router uses when the chain breaks.

Submission policy
-----------------
RouterArena's submission policy prohibits training on RouterArena data.
This adapter's mappings are hand-tuned from the production router's
existing routing tables and were not fit on RouterArena labels.
"""

