def _prompt_hash(text: str) -> str:
    return hashlib.sha1(text.encode("utf-8", errors="replace")).hexdigest()


def _subject_override(text: str) -> str | None:
    """Look up pre-classified subject for this prompt and return model.

    Returns None if no cached subject exists.
    """
    if not _SUBJECT_CACHE:
        return None
    h = _prompt_hash(text)
    subject = _SUBJECT_CACHE.get(h)
    if subject is None:
        return None
    return _SUBJECT_TO_MODEL.get(subject)


def _classify_complexity(text: str, task_type: str) -> str:
    """Return one of: simple, moderate, complex, deep_reasoning.

    Length-based bumps are deliberately conservative: benchmark prompts
    routinely carry 500–2000 chars of context wrapper without being
    reasoning-complex. We only declare *complex* when:
      (a) the prompt explicitly signals complexity (architect/comprehensive/...)
      (b) OR the prompt is >2000 chars AND has no benchmark-wrapper signal
    """
    if _RE_DEEP_REASONING.search(text):
        return "deep_reasoning"
    if _RE_COMPLEX.search(text):
        return "complex"
    if _RE_SIMPLE.search(text):
        return "simple"
    # Benchmark MC / RC / templated knowledge questions → moderate at most.
    if _RE_BENCHMARK_MC.search(text):
        return "moderate"
    if len(text) > 2000:
        return "complex"
    if len(text) > 150:
        return "moderate"
    return "simple" if task_type == "query" else "moderate"


def _classify_task_type(text: str) -> str:
    """Score task-type signals; return the strongest match.

    Order matters when ties happen: code > research > analyze > generate > query.
    These priorities mirror the production hook's preference for the more
    capability-sensitive task type when patterns overlap.

    Benchmark fast-paths short-circuit the scoring for known templated wrappers
    so we don't misclassify a knowledge-MC question as `analyze` just because
    its content mentions "evaluate" or "compare".
    """
    if _RE_BENCHMARK_CODE.search(text):
        return "code"
    if _RE_BENCHMARK_MATH.search(text):
        return "analyze"
    if _RE_BENCHMARK_MC.search(text):
        return "query"
    scores = {
        "code": len(_RE_CODE.findall(text)),
        "research": len(_RE_RESEARCH.findall(text)),
        "analyze": len(_RE_ANALYZE.findall(text)),
        "generate": len(_RE_GENERATE.findall(text)),
        "query": len(_RE_QUERY.findall(text)),
    }
    best = max(scores.items(), key=lambda kv: kv[1])
    if best[1] == 0:
        return "query"
    return best[0]


